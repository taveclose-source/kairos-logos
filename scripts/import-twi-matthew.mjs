/**
 * Generate SQL UPDATE statements for Matthew 1-8 Twi translations.
 *
 * Reads JSON scaffold files from the Bible\Matthew\ folder and prints
 * SQL to stdout — one UPDATE per chapter using a VALUES CTE approach.
 *
 * Usage:
 *   node scripts/import-twi-matthew.mjs > twi-matthew-update.sql
 */
import { readFileSync, existsSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const BIBLE_DIR = join(__dirname, '..', '..', 'Bible', 'Matthew')

// JSON file patterns per chapter
const chapterFiles = {
  1: 'Matthew1_Twi_Interlinear_Scaffold.json',
  2: 'Matthew2_Twi_Interlinear_Scaffold.json',
  3: 'Matthew3_Twi_Interlinear_Scaffold.json',
  4: 'Matthew4_Twi_Interlinear_Scaffold.json',
  5: 'Matthew5_Twi_Interlinear_Scaffold.json',
  6: 'Matthew6_Twi_Interlinear_Scaffold_v2.json',
  7: 'Matthew7_Scaffold.json',
  8: 'Matthew8_Scaffold.json',
}

/** Escape single quotes for SQL string literals */
function esc(str) {
  return str.replace(/'/g, "''")
}

console.log('-- Twi translations for Matthew 1-8')
console.log('-- Generated by import-twi-matthew.mjs')
console.log(`-- Date: ${new Date().toISOString()}`)
console.log()

let totalVerses = 0

for (const [chapter, filename] of Object.entries(chapterFiles)) {
  const filePath = join(BIBLE_DIR, chapter, filename)

  if (!existsSync(filePath)) {
    console.log(`-- WARNING: Skipping chapter ${chapter} — file not found: ${filePath}`)
    console.log()
    continue
  }

  const raw = readFileSync(filePath, 'utf-8')
  const data = JSON.parse(raw)
  const verses = data.verses

  if (!verses || verses.length === 0) {
    console.log(`-- WARNING: Chapter ${chapter} has no verses`)
    console.log()
    continue
  }

  // Build VALUES rows
  const rows = []
  for (const v of verses) {
    // Extract verse number from ref like "Matthew 1:3", or fall back to .verse field
    const verseNum = parseInt(v.ref?.split(':')[1]) || parseInt(v.verse)
    if (!verseNum || !v.twi_text) continue

    rows.push(`  (${parseInt(chapter)}, ${verseNum}, '${esc(v.twi_text)}')`)
  }

  if (rows.length === 0) {
    console.log(`-- WARNING: Chapter ${chapter} produced no valid rows`)
    console.log()
    continue
  }

  totalVerses += rows.length

  console.log(`-- Chapter ${chapter}: ${rows.length} verses`)
  console.log(`UPDATE bible_verses SET twi_text = v.twi_text, has_twi = true`)
  console.log(`FROM (VALUES`)
  console.log(rows.join(',\n'))
  console.log(`) AS v(chapter, verse, twi_text)`)
  console.log(`JOIN bible_books bb ON bb.book_name = 'Matthew'`)
  console.log(`WHERE bible_verses.book_id = bb.id`)
  console.log(`AND bible_verses.chapter = v.chapter`)
  console.log(`AND bible_verses.verse = v.verse;`)
  console.log()
}

console.log(`-- Total: ${totalVerses} verses across ${Object.keys(chapterFiles).length} chapters`)
